package view;

import com.sun.glass.events.KeyEvent;

/**
 *
 * This class contains the control key settings for the ExamView
 */
public class ControlKeys {

    public static final int nextKeyCode = KeyEvent.VK_RIGHT;//the key for next question
    public static final int previousKeyCode = KeyEvent.VK_LEFT;//the key for previous question
    public static final int option1 = KeyEvent.VK_1;//the key for choosing the first option
    public static final int option2 = KeyEvent.VK_2;//the key for choosing the second option
    public static final int option3 = KeyEvent.VK_3;//the key for choosing the third option
    public static final int readQuestion = KeyEvent.VK_SPACE;//the key to read the question
    public static final int readOption1 = KeyEvent.VK_NUMPAD1;//the key to read the first option
    public static final int readOption2 = KeyEvent.VK_NUMPAD2;//the key to read the second option
    public static final int readOption3 = KeyEvent.VK_NUMPAD3;//the key to read the third option
    public static final int submit = KeyEvent.VK_ENTER;//the key to submit answers

}
